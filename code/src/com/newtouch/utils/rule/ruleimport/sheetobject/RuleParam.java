package com.newtouch.utils.rule.ruleimport.sheetobject;

public class RuleParam {
	// 规则名称
	private String ruleName;
	// 参数名称
	private String paramName;
	// 参数编码
	private String paramCode;
	// 参数类别
	private String paramSort;
	// 参数类型
	private String paramType;
	// 参数顺序
	private String paramOrder;

	public String getRuleName() {
		return ruleName;
	}

	public void setRuleName(String ruleName) {
		this.ruleName = ruleName;
	}

	public String getParamName() {
		return paramName;
	}

	public void setParamName(String paramName) {
		this.paramName = paramName;
	}

	public String getParamCode() {
		return paramCode;
	}

	public void setParamCode(String paramCode) {
		this.paramCode = paramCode;
	}

	public String getParamSort() {
		return paramSort;
	}

	public void setParamSort(String paramSort) {
		this.paramSort = paramSort;
	}

	public String getParamType() {
		return paramType;
	}

	public void setParamType(String paramType) {
		this.paramType = paramType;
	}

	public String getParamOrder() {
		return paramOrder;
	}

	public void setParamOrder(String paramOrder) {
		this.paramOrder = paramOrder;
	}

}
