package com.newtouch.utils.rule.ruleimport.sheetobject;

public class CallParam {
	// 功能编码
	private String funcCode;
	// 分组名称
	private String groupName;
	// 参数编码
	private String paramCode;
	// 参数名称
	private String paramName;
	// 对应属性
	private String paramAttribute;

	public String getFuncCode() {
		return funcCode;
	}

	public void setFuncCode(String funcCode) {
		this.funcCode = funcCode;
	}

	public String getGroupName() {
		return groupName;
	}

	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

	public String getParamCode() {
		return paramCode;
	}

	public void setParamCode(String paramCode) {
		this.paramCode = paramCode;
	}

	public String getParamName() {
		return paramName;
	}

	public void setParamName(String paramName) {
		this.paramName = paramName;
	}

	public String getParamAttribute() {
		return paramAttribute;
	}

	public void setParamAttribute(String paramAttribute) {
		this.paramAttribute = paramAttribute;
	}

}
